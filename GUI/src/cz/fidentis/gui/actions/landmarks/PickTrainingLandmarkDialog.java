    private void saveChoiceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChoiceButtonActionPerformed
        HashMap<Integer, Boolean> newLandmarks = new HashMap<>();
        
        for(int i = 0; i < activeList.getModel().getSize(); i++){
            newLandmarks.put(FpTexter.getInstance().getTypeFromName(activeList.getModel().getElementAt(i)), Boolean.TRUE);
        }
        
        for(int i = 0; i < inactiveList.getModel().getSize(); i++){
            newLandmarks.put(FpTexter.getInstance().getTypeFromName(inactiveList.getModel().getElementAt(i)), Boolean.FALSE);
        }
        
        parent.setUsedLandmarks(newLandmarks);
        this.dispose();
    }//GEN-LAST:event_saveChoiceButtonActionPerformed

    private void makeInactiveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_makeInactiveButtonActionPerformed
        List<String> selected = activeList.getSelectedValuesList();
        
        for(String lm : selected){
            ((DefaultListModel) activeList.getModel()).removeElement(lm);
            insert((DefaultListModel) inactiveList.getModel(), lm);
        }
       
    }//GEN-LAST:event_makeInactiveButtonActionPerformed

    private void makeActiveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_makeActiveButtonActionPerformed
        List<String> selected = inactiveList.getSelectedValuesList();
        
        for(String lm : selected){
            ((DefaultListModel) inactiveList.getModel()).removeElement(lm);
            insert((DefaultListModel) activeList.getModel(), lm);
        }
       
    }//GEN-LAST:event_makeActiveButtonActionPerformed

    private void insert(DefaultListModel model, String landmarkName){
        int index = FpTexter.getInstance().getTypeFromName(landmarkName);
        
        if(model.size() == 0 || index < FpTexter.getInstance().getTypeFromName((String)model.firstElement())){
            model.add(0, landmarkName);
            return;
        }
        
        for(int i = 0; i < model.getSize() - 1; i++){
            int above = FpTexter.getInstance().getTypeFromName((String)model.getElementAt(i));
            int below = FpTexter.getInstance().getTypeFromName((String)model.getElementAt(i+1));
            
            if(index > above && index < below){
                model.add(i+1, landmarkName);
                return;
            }         
        }
        
        model.addElement(landmarkName);
    }

