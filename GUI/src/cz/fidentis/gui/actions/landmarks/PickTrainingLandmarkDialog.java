    private void setInfo(LandmarkTrainDialogueTopComponent parent, HashMap<Integer, Boolean> landmarksUsage){
        this.parent = parent;
        DefaultListModel active = new DefaultListModel();
        DefaultListModel inactive = new DefaultListModel();
        List<Integer> sortedIndices = SortUtils.instance().asSortedList(landmarksUsage.keySet());
        
        for(Integer i : sortedIndices){
            if(landmarksUsage.get(i))
                active.addElement(FpTexter.getInstance().getFPname(i));
            else
                inactive.addElement(FpTexter.getInstance().getFPname(i));
        }
        
        activeList.setModel(active);
        inactiveList.setModel(inactive);               
    }

