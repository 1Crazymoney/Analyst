    private void confirmButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmButtonMouseClicked
        if (canvas1 == null && canvas2 != null) {
            ResultExports.instance().exportVisualResults(parent, canvas2, (Integer) widthSpinner.getValue(), (Integer) heightSpinner.getValue(), originalWidth1, originalHeigth1);
            
        } else if (canvas1 != null && canvas2 == null) {
            ResultExports.instance().exportVisualResults(parent, canvas1, (Integer) widthSpinner.getValue(), (Integer) heightSpinner.getValue(), originalWidth1, originalHeigth1);
        } else if (canvas1 != null && canvas2 != null) {
            ResultExports.instance().exportVisualResults(parent, canvas1, canvas2, (Integer) widthSpinner.getValue(), (Integer) heightSpinner.getValue(), 
                    originalWidth1, originalHeigth1, originalWidth2, originalHeigth2);
        }

        
        cancelButtonMouseClicked(evt);
    }//GEN-LAST:event_confirmButtonMouseClicked

